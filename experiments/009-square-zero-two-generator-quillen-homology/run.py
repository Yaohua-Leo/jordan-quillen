"""Run Experiment 009 for the two-generator square-zero Jordan algebra."""

from __future__ import annotations

import json
import sys
from collections import Counter
from pathlib import Path
from textwrap import wrap
from typing import Any

EXPERIMENT_DIR = Path(__file__).resolve().parent
REPO_ROOT = EXPERIMENT_DIR.parents[1]
EXPECTED_PATH = EXPERIMENT_DIR / "expected.json"
RESULTS_PATH = EXPERIMENT_DIR / "results.json"
CHAIN_COMPLEX_PATH = EXPERIMENT_DIR / "data" / "chain_complex.json"
TEX_PATH = EXPERIMENT_DIR / "tex" / "exp009_square_zero_two_gen_quillen.tex"


def load_expected() -> dict[str, Any]:
    return json.loads(EXPECTED_PATH.read_text(encoding="utf-8"))


def write_json(path: Path, payload: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(
        json.dumps(payload, indent=2, sort_keys=True) + "\n",
        encoding="utf-8",
    )


def main() -> int:
    sys.path.insert(0, str(REPO_ROOT / "src"))
    expected = load_expected()

    try:
        from jordan_qh.square_zero_two_gen import exp009_run_weight_bounds
    except (ImportError, ModuleNotFoundError) as exc:
        write_json(
            RESULTS_PATH,
            {
                "status": "not run yet",
                "passed": None,
                "notes": (
                    "src/jordan_qh/square_zero_two_gen.py is not available; "
                    "Experiment 009 has not produced even the degree-one "
                    "sanity record."
                ),
                "error": str(exc),
            },
        )
        return 0

    weight_bounds = expected["weight_bounds"]
    run_record = exp009_run_weight_bounds(weight_bounds=tuple(weight_bounds))
    chain_record = run_record["selected"]
    selected_weight_bound = chain_record["weight_bound"]
    write_json(CHAIN_COMPLEX_PATH, chain_record)
    TEX_PATH.parent.mkdir(parents=True, exist_ok=True)
    TEX_PATH.write_text(render_tex_report(chain_record), encoding="utf-8")

    passed = bool(chain_record["passed"])
    write_json(
        RESULTS_PATH,
        {
            "status": "run",
            "passed": passed,
            "experiment": expected["experiment"],
            "selected_weight_bound": selected_weight_bound,
            "weight_bound_stability": run_record["stability"],
            "weight_bound_summaries": run_record["summaries"],
            "tex_report_generated": True,
            "result_is_stable": run_record["stability"]["stable"],
            "result_is_truncated_evidence_only": not run_record["stability"]["stable"],
            "computed_homology_dimensions": chain_record["linear_algebra"][
                "homology_dimensions"
            ],
            "checks": chain_record["checks"],
            "paths": {
                "chain_complex_json": str(CHAIN_COMPLEX_PATH.relative_to(REPO_ROOT)),
                "tex_report": str(TEX_PATH.relative_to(REPO_ROOT)),
            },
            "notes": (
                "The runner constructed the fixed example-specific bounded "
                "ordinary Jordan replacement through degree 6 for W = 6, 7, "
                "8. The selected W = 8 output is truncated evidence because "
                "the weight-bound comparison is not stable."
            ),
        },
    )
    return 0


def render_tex_report(chain_record: dict[str, Any]) -> str:
    """Render a conservative TeX report from the chain-complex record."""
    dimensions = chain_record["linear_algebra"]["homology_dimensions"]
    stability = chain_record["weight_bound_stability"]
    basis_sizes = {
        degree: len(basis) for degree, basis in chain_record["bases"].items()
    }
    lines = [
        r"\documentclass[11pt]{article}",
        r"\usepackage[T1]{fontenc}",
        r"\usepackage{amsmath,amssymb}",
        r"\usepackage{booktabs}",
        r"\usepackage{geometry}",
        r"\usepackage[hidelinks]{hyperref}",
        r"\geometry{margin=1in}",
        "",
        r"\title{Experiment 009: Summary Report}",
        r"\author{Generated by run.py}",
        r"\date{}",
        "",
        r"\begin{document}",
        r"\maketitle",
        "",
        r"\section{Audit Data}",
        "This TeX file is intentionally a compact summary.  The complete "
        "auditable record, including all bases, full differentials, boundary "
        "matrices, kernels, images, and representative coordinates, is stored "
        "in",
        r"\[",
        r"\href{../data/chain_complex.json}{\texttt{../data/chain\_complex.json}}.",
        r"\]",
        "",
        r"\section{Dimension Tables}",
        "Selected weight bound: "
        f"$W={chain_record['weight_bound']}$.  Resolution degree: "
        f"{chain_record['resolution_degree']}.",
        "",
        _cell_summary_table(chain_record),
        "",
        _basis_size_table(basis_sizes),
        "",
        _homology_dimension_table(dimensions),
        "",
        r"\section{Key Matrices and Representatives}",
        "The full boundary matrices and representative coordinates are in the "
        "JSON audit file.  Matrix rows are source basis elements and columns "
        "are target coordinates.",
        "",
        _boundary_summary_table(chain_record),
        "",
        r"\subsection{Boundary Formula Summary}",
        _boundary_formula_preview(chain_record),
        "",
        r"\subsection{Representative Summary}",
        _representative_preview(chain_record),
        "",
        r"\section{Weight-Bound Stability}",
        "The requested bounds $W=6,7,8$ were compared.  The selected output "
        "may be called stable only if the dimensions and representatives "
        "agree across these bounds.",
        "",
        _stability_table(stability),
        "",
        "Stability verdict: "
        + _latex_escape(str(stability["interpretation"]))
        + ".",
        "",
        r"\end{document}",
        "",
    ]
    return "\n".join(lines)


def _cell_summary_table(chain_record: dict[str, Any]) -> str:
    counts = Counter(
        (int(cell["degree"]), int(cell["weight"]))
        for cell in chain_record["cells"]
    )
    rows = [
        r"\begin{center}",
        r"\begin{tabular}{rrr}",
        r"\toprule",
        r"Degree & Weight & Cell count \\",
        r"\midrule",
    ]
    for degree, weight in sorted(counts):
        rows.append(f"{degree} & {weight} & {counts[(degree, weight)]} \\\\")
    rows.extend([r"\bottomrule", r"\end{tabular}", r"\end{center}"])
    return "\n".join(rows)


def _basis_size_table(basis_sizes: dict[str, int]) -> str:
    rows = [
        r"\begin{center}",
        r"\begin{tabular}{rrrrrrr}",
        r"\toprule",
        r"\(C_0\) & \(C_1\) & \(C_2\) & \(C_3\) & \(C_4\) & "
        r"\(C_5\) & \(C_6\) \\",
        r"\midrule",
        " & ".join(str(basis_sizes[f"C{degree}"]) for degree in range(7)) + r" \\",
        r"\bottomrule",
        r"\end{tabular}",
        r"\end{center}",
    ]
    return "\n".join(rows)


def _boundary_summary_table(chain_record: dict[str, Any]) -> str:
    matrices = chain_record["boundary_matrices_source_rows"]
    ranks = chain_record["linear_algebra"]["ranks"]
    rows = [
        r"\begin{center}",
        r"\begin{tabular}{rrrrr}",
        r"\toprule",
        r"Boundary & Source dim & Target dim & Rank & Nonzero rows \\",
        r"\midrule",
    ]
    for degree in range(1, 7):
        matrix = matrices[f"partial_{degree}"]
        source_dim = len(matrix)
        target_dim = len(matrix[0]) if matrix else len(
            chain_record["bases"].get(f"C{degree - 1}", []),
        )
        nonzero_rows = sum(any(entry != 0 for entry in row) for row in matrix)
        rows.append(
            rf"\(\partial_{degree}\) & {source_dim} & {target_dim} & "
            f"{ranks[f'partial_{degree}']} & {nonzero_rows} \\\\",
        )
    rows.extend([r"\bottomrule", r"\end{tabular}", r"\end{center}"])
    return "\n".join(rows)


def _homology_dimension_table(dimensions: dict[str, int]) -> str:
    rows = [
        r"\begin{center}",
        r"\begin{tabular}{rrrrrr}",
        r"\toprule",
        r"\(H_0\) & \(H_1\) & \(H_2\) & \(H_3\) & \(H_4\) & \(H_5\) \\",
        r"\midrule",
        " & ".join(str(dimensions[f"H{degree}"]) for degree in range(6)) + r" \\",
        r"\bottomrule",
        r"\end{tabular}",
        r"\end{center}",
    ]
    return "\n".join(rows)


def _stability_table(stability: dict[str, Any]) -> str:
    comparison = stability["dimension_comparison"]
    rows = [
        r"\begin{center}",
        r"\begin{tabular}{rrrrrrr}",
        r"\toprule",
        r"Weight & \(H_0\) & \(H_1\) & \(H_2\) & \(H_3\) & "
        r"\(H_4\) & \(H_5\) \\",
        r"\midrule",
    ]
    for weight in stability["weight_bounds"]:
        dims = comparison[str(weight)]
        rows.append(
            f"{weight} & "
            + " & ".join(str(dims[f"H{degree}"]) for degree in range(6))
            + r" \\",
        )
    rows.extend([r"\bottomrule", r"\end{tabular}", r"\end{center}"])
    return "\n".join(rows)


def _boundary_formula_preview(
    chain_record: dict[str, Any],
    *,
    limit: int = 4,
) -> str:
    formulas = chain_record["indecomposable_differentials"]
    blocks: list[str] = []
    for degree in range(1, 7):
        prefix = f"partial_{degree}("
        nonzero = [
            f"{name} = {value}"
            for name, value in formulas.items()
            if name.startswith(prefix) and value != "0"
        ]
        if not nonzero:
            preview = [f"partial_{degree}: all recorded formulas are zero."]
        else:
            preview = nonzero[:limit]
            remaining = len(nonzero) - len(preview)
            if remaining:
                preview.append(
                    f"... {remaining} more nonzero formulas in chain_complex.json"
                )
        blocks.append(rf"\paragraph{{\(\partial_{degree}\).}}")
        blocks.append(_verbatim_block(preview))
    return "\n".join(blocks)


def _representative_preview(
    chain_record: dict[str, Any],
    *,
    limit: int = 5,
) -> str:
    representatives = chain_record["linear_algebra"]["homology_representative_formulas"]
    blocks: list[str] = []
    for degree in range(6):
        label = f"H{degree}"
        formulas = representatives[label]
        preview = formulas[:limit]
        if len(formulas) > limit:
            preview.append(
                f"... {len(formulas) - limit} more representatives in "
                "chain_complex.json"
            )
        blocks.append(rf"\paragraph{{\(H_{degree}\).}}")
        blocks.append(_verbatim_block(preview or ["0"]))
    return "\n".join(blocks)


def _verbatim_block(lines: list[str], *, width: int = 92) -> str:
    wrapped: list[str] = []
    for line in lines:
        wrapped.extend(wrap(line, width=width, break_long_words=False) or [""])
    return "\n".join([r"\begin{verbatim}", *wrapped, r"\end{verbatim}"])


def _latex_escape(value: str) -> str:
    replacements = {
        "\\": r"\textbackslash{}",
        "_": r"\_",
        "&": r"\&",
        "%": r"\%",
        "$": r"\$",
        "#": r"\#",
        "{": r"\{",
        "}": r"\}",
    }
    return "".join(replacements.get(char, char) for char in value)


